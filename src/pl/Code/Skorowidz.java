package pl.Code;

public class Skorowidz {

	String slowo;
	int wiersz;

	public Skorowidz(String slowo, int wiersz) {

		this.slowo = slowo;
		this.wiersz = wiersz;

	}

	public String getSlowo() {
		return slowo;
	}

	public void setSlowo(String slowo) {
		this.slowo = slowo;
	}

	public int getWiersz() {
		return wiersz;
	}

	public void setWiersz(int wiersz) {
		this.wiersz = wiersz;
	}

}
